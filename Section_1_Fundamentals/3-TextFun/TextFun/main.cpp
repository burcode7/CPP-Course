#include <iostream>
#include <string> // Includes the string library for the string variable

using namespace std;

int main()
{
	// For char variables, use single quotes. For string variables, use double quotes.

	char singleChar = 'a'; // "character" variable that holds a character

	string myName = "Ben"; // "string" variable that holds text

	cout << singleChar << endl;
	cout << "Hello " << myName << endl;

	return 0;
}